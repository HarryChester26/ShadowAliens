package game.utils;

public class Point {
  private double x;
  private double y;

  public Point(double x, double y) {
    this.x = x;
    this.y = y;
  }

  public double getX() {
    return x;
  }

  public double getY() {
    return y;
  }

  public void setX(double data) {
    x = data;
  }

  public void setY(double data) {
    y = data;
  }
}
